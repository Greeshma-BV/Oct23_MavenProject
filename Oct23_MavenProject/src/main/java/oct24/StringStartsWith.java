package oct24;

public class StringStartsWith 
{

	public static void main(String[] args) 
	{
		String s1="Hello world";
		System.out.println(s1.startsWith("He"));
		System.out.println(s1.startsWith("he"));
		System.out.println(s1.startsWith("ma"));
		System.out.println(s1.startsWith("wo"));


	}

}
